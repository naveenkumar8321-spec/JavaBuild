package MainPackage;

public class ForLopp {

    public static void main (String args[])
    {
        //print even num 1 to 10
        //for loop

        for(int i=1;i<=10;i++)
        {
            if(i%2 == 0)
            {
                System.out.println(i + " is Even Number");
            }
        }

    }
}
